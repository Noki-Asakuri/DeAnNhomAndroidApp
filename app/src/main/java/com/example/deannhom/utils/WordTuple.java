package com.example.deannhom.utils;

public class WordTuple {
    public String word;
    public String title;
    public String definition;
    public String audioUrl;

    public WordTuple(String word, String title, String definition, String audioUrl) {
        this.word = word;
        this.title = title;
        this.definition = definition;
        this.audioUrl = audioUrl;
    }
}
